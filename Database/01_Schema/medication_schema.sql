/*
 유저 테이블 
- account_type 컬럼에서 확장성을 위해 BOOLEAN 대신 SMALLINT 채택 및 CHECK 제약조건 추가. 
 */
CREATE TABLE users (
    user_id INTEGER GENERATED BY DEFAULT AS IDENTITY
	,login_id VARCHAR(50) NOT NULL
	,password VARCHAR(255) NOT NULL
	,name VARCHAR(50) NOT NULL 
	,email VARCHAR(100) NOT NULL 
	,birth_date DATE NOT NULL
	,account_type SMALLINT NOT NULL               -- 역할 구분: 0(PATIENT), 1(GUARDIAN)
	,created_at TIMESTAMP DEFAULT NOW() NOT NULL 
	,updated_at TIMESTAMP DEFAULT NOW() NOT NULL
	,deleted_at TIMESTAMP                      -- NULL이면 활성화 상태 
	,CONSTRAINT pk_user_id PRIMARY KEY (user_id)
	,CONSTRAINT uq_login_id UNIQUE (login_id)
	,CONSTRAINT uq_user_email UNIQUE (email)
	,CONSTRAINT ck_account_type CHECK (account_type IN(0,1))
);
/*
 사용자-하드웨어 매핑 테이블
 - 앱에서 기기 등록 시 매핑됨.
 - 하드웨어가 보내는 device_code를 통해 어느 사용자의 기기인지 식별.
 - 사용자와 1:N 관계로 사용자는 여러 하드웨어를 지닐 수 있음. 
*/
CREATE TABLE hardwares (
    hardware_id INTEGER GENERATED BY DEFAULT AS IDENTITY
    ,user_id INTEGER NOT NULL
    ,device_code VARCHAR(50) NOT NULL             -- ESP32 기기 고유 번호 등
    ,device_name VARCHAR(50) NOT NULL
    ,registered_at TIMESTAMP DEFAULT NOW() NOT NULL
    ,updated_at TIMESTAMP DEFAULT NOW() NOT NULL    -- 기기 정보(별명 등) 수정 시
    ,deleted_at TIMESTAMP                           -- 기기 연결 해제 시 
    ,CONSTRAINT pk_hardware_id PRIMARY KEY (hardware_id)
    ,CONSTRAINT fk_hardware_user_id FOREIGN KEY (user_id) REFERENCES users (user_id) ON DELETE CASCADE
    ,CONSTRAINT uq_hardware_device_code UNIQUE (device_code)
);
/*
NFC 스티커 테이블 
- 실시간으로 어떤 스케줄에 어떤 스티커가 매핑되어 있는지 관리. 
- 새 스티커를 쓰면 nfc_uid만 업데이트. 
- 복약 스케줄과 1:N 관계로 복약 스케줄이 삭제되면 자동 삭제됨.
*/
CREATE TABLE tag_mappings (
    mapping_id INTEGER GENERATED BY DEFAULT AS IDENTITY   
    ,user_id INTEGER NOT NULL
    ,tag_uid VARCHAR(50) NOT NULL          -- nfc스티커 고유 번호       
    ,created_at TIMESTAMP DEFAULT NOW() NOT NULL 
    ,last_tagged_at TIMESTAMP      
    ,deleted_at TIMESTAMP
    ,CONSTRAINT pk_mapping_id PRIMARY KEY (mapping_id)
    ,CONSTRAINT fk_mapping_user_id FOREIGN KEY (user_id) REFERENCES users (user_id) ON DELETE CASCADE
	,CONSTRAINT uq_mapping_tag_uid UNIQUE (tag_uid)
 );
/*
복약 스케줄 테이블
- 사용자가 설정한 '알림'이나'계획'.
- 스티커를 버려도 스케줄 기록은 남음. 
- 기기가 없는 사용자를 고려해여 mapping_id의 not null 제약 조건을 삭제함. 
 */
CREATE TABLE medication_schedule (
    schedule_id INTEGER GENERATED BY DEFAULT AS IDENTITY
    ,user_id INTEGER NOT NULL
	,mapping_id INTEGER 
	,schedule_name VARCHAR(100) NOT NULL
	,calibrated_weight DECIMAL(5,2) 
	,created_at TIMESTAMP DEFAULT NOW() NOT NULL 
	,updated_at TIMESTAMP DEFAULT NOW() NOT NULL
	,deleted_at TIMESTAMP 
	,CONSTRAINT pk_schedule_id PRIMARY KEY (schedule_id) 
	,CONSTRAINT fk_schedule_user_id FOREIGN KEY (user_id) REFERENCES users (user_id) ON DELETE CASCADE
	,CONSTRAINT fk_schedule_mapping_id FOREIGN KEY (mapping_id) REFERENCES tag_mappings (mapping_id)
	,CONSTRAINT uq_schedule_mapping_id UNIQUE (mapping_id)
);
/*
 복약 시간 테이블
 - 복약 스케줄과 1:N 관계로 복약 스케줄이 삭제되면 자동 삭제됨.
 */
CREATE TABLE schedule_time (
	time_id INTEGER GENERATED BY DEFAULT AS IDENTITY
	,schedule_id INTEGER NOT NULL
	,take_time TIME NOT NULL
	,days_of_week SMALLINT NOT NULL
	,created_at TIMESTAMP DEFAULT NOW() NOT NULL
	,updated_at TIMESTAMP DEFAULT NOW() NOT NULL
    ,deleted_at TIMESTAMP
	,CONSTRAINT pk_time_id PRIMARY KEY (time_id)
	,CONSTRAINT fk_time_schedule_id FOREIGN KEY (schedule_id) REFERENCES medication_schedule (schedule_id) ON DELETE CASCADE
	,CONSTRAINT ck_days_range CHECK (days_of_week BETWEEN 0 AND 127)
);
/*
복약 로그 테이블
- 하드웨어 센서의 측정값과 복약 성공 여부 기록.
- 부모 스케줄이 삭제(Soft Delete)되어도 기록을 보존.
- 보호자(account_type=1)의 경우 기기 별 관리/조회 시 성능을 위해 hardware_id fk 제약조건 추가.
- status 컬럼에서 확장성을 위해 BOOLEAN 대신 SMALLINT 채택 및 CHECK 제약조건 추가. 
*/
CREATE TABLE medication_logs(
	log_id BIGINT GENERATED BY DEFAULT AS IDENTITY
	,user_id INTEGER NOT NULL
	,hardware_id INTEGER NOT NULL 
	,mapping_id INTEGER NOT NULL
	,schedule_id INTEGER NOT NULL
	,scheduled_time TIMESTAMP       -- 정상 복용시 서버에서 [날짜+시간], 예정에 없는 날짜의 복용 [NULL] 
	,measured_weight DECIMAL(5,2) NOT NULL
	,status SMALLINT NOT NULL                      -- 0(FAIL), 1(AUTO_SUCCESS), 2(MANUAL_CONFIRMED)
	,created_at TIMESTAMP DEFAULT NOW() NOT NULL   -- 복약 당시 시간 
	,updated_at TIMESTAMP DEFAULT NOW() NOT NULL
	,CONSTRAINT pk_log_id PRIMARY KEY (log_id)
	,CONSTRAINT fk_log_mapping_id FOREIGN KEY (mapping_id) REFERENCES tag_mappings (mapping_id) 
	,CONSTRAINT fk_log_schedule_id FOREIGN KEY (schedule_id) REFERENCES medication_schedule (schedule_id)
	,CONSTRAINT fk_log_user_id FOREIGN KEY (user_id) REFERENCES users (user_id) 
	,CONSTRAINT fk_log_hardware_id FOREIGN KEY (hardware_id) REFERENCES hardwares (hardware_id) 
	,CONSTRAINT ck_status_range CHECK (status IN(0,1,2))
);